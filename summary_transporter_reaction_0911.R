############## function: fixed_lines ####
#' Title
#' Correct the line that includes the compound whose name has a comma by replacing comma with question mark
#' @param sample output file that generated from HTSeq-count (*HTSeq_sorted.csv)
#'
#' @return a dataframe with wrong line fixed
#' @export
#'
#' @examples
fixed_lines<-function(sample){
  
  lines <- readLines(sample)
  fixed_lines <- sapply(lines, function(line) {
    parts <- strsplit(line, ",")[[1]]
    if (length(parts) > 2) {
      # collapse all but the last part
      first_column_ele<-paste(parts[-length(parts)], collapse = ",")
      first_column_modified<-sub(',','?',first_column_ele) ### replace the comma with question mark to avoid being recognized as new column
      
      print(paste0('the line was fixed: ',paste(c(first_column_modified, parts[length(parts)]), collapse = ",")))
      return(paste(c(first_column_modified, parts[length(parts)]), collapse = ","))
    } else {
      return(line)
    }
  })
  transporter_expressed <- read.csv(textConnection(fixed_lines), stringsAsFactors = FALSE, header = FALSE, col.names = c('Name','Reads_count'))
  
  return(transporter_expressed)
}

######## add the reactions id to the dataframe ###
### the reactions id were extracted from transporter_tbl file (e.g. bin_3_merged_2017-Transporter.tbl) ###
### the input form of transporter_count is like this '3.A.1.8.2_molybdate_cpd11574' ###

reaction_id<-function(rowname){
  if (!is.null(opt$Reaction_tbl)){
    ### get the biocyc_id from df merged_final_sorted
    rxn_id<-strsplit(rowname, '_')[[1]][1]
    
    specific_row<-reaction_tbl[reaction_tbl$rxn==rxn_id,][1,]
    
    # If no match found, return NA
    if (nrow(specific_row) == 0) {
      return(NA)
    }
    ### If multiple matches found, retrieve only the first one
    else if (nrow(specific_row) > 1) {
      return(specific_row[1,]$dbhit)
    }
    else{
      return(specific_row$dbhit)
    }
  }
  try({
    tcdb_id <- strsplit(rowname, '_')[[1]][1]
    cpd_name <- sub('\\?',',',strsplit(rowname, '_')[[1]][2]) ### replace the ? to comma to have the compound matching its real name
    
    # If no underscore exists in rowname, this line will raise an error 
    # and the function will return NA
    if (length(c(tcdb_id, cpd_name)) < 2) {
      return(NA)
    }
    
    specific_row <- transporter_tbl[transporter_tbl$tc == tcdb_id & transporter_tbl$sub == cpd_name,]
    
    # If no match found, return NA
    if (nrow(specific_row) == 0) {
      return(NA)
    }
    
    # If multiple matches found, return them concatenated (or handle in another way)
    if (nrow(specific_row) > 1) {
      return(paste(specific_row$rea, collapse = "; "))
    }
    
    return(specific_row$rea)
  }, silent = TRUE)
}

