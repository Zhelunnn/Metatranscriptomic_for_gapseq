####### check if the ex_cpd (e.g. cpd00037[1]) is at the left or right side of the reacting mark '<=>' #####
cpd_position_in_equation<-function(equation,ex_cpd_name){
  
  ### judge if the desired cpd is in the equation or not ### 
  reg_result=regexpr(ex_cpd_name, equation)
  if (length(reg_result)>=1){
    position_cpd <- reg_result[[1]]
  }else{
    return(NULL)
  }
  
  position_symbol <- regexpr("=", equation)[[1]]
  
  # Check the relative positions
  if (position_cpd < position_symbol) {
    return('left')
  } else if (position_cpd > position_symbol) {
    return('right')
  } else {
    print('This scenario should not happen with a well-structured equation')
  }
}

### input one row from df_transporter_count, the function will return transporter direction based on reactions database 'seed_reactions_corrected.tsv' ###
### the input form of transporter_count is like this '3.A.1.8.2_molybdate_cpd11574' ###
#' Title
#'
#' @param line_count one row from merged_final_sorted(dataframe of reads count with reactions_id added)
#'
#' @return direction of each equation in the reactions_id grids
#' @export
#'
#' @examples
#' input 3.A.1.6.1_sodium_cpd00971	0	0	1	0.333333333333333	0.471404520791032	rxn05622,rxn34391,rxn34513
#' output	uptake uptake uptake

check_direction<-function(line_count){
  
  if (is.na(line_count['reactions_id'])|is.null(line_count['reactions_id'])){
    return(NULL)
  }
  rxns_id<-strsplit(line_count['reactions_id'],',')[[1]]
  rxn_equation<-sapply(rxns_id,function(x){reaction_eq_db[which(reaction_eq_db$id==x),]$equation})
  
  cpd_id<-strsplit(line_count['rowname'], '_')[[1]][3]
  ex_cpd_id<-paste0(cpd_id,'\\[1\\]',sep='')
  
  #### judge if the cpd can be uptaken or released or both by checking the compound position in the equation and reacting direction ###
  result<-sapply(rxn_equation,function(equation){
    if(length(equation)==0){
      return(NULL)
    }
    position<-cpd_position_in_equation(equation,ex_cpd_id)
    
    if (grepl('<=>',equation)){
      return('bidirectional')
    }else if(grepl('<=',equation) & position=='left'){
      return('release')
    }else if(grepl('=>',equation) & position=='left'){
      return('uptake')
    }else if(grepl('<=',equation) & position=='right'){
      return('uptake')
    }else if(grepl('=>',equation) & position=='right'){
      return('release')
      #### if the reaction is not included in the database, database seed_reactions will used to do further search ###
    }else{
      print(paste0('please check the compound: ',cpd_id,' and its equation',sep=''))
      return(NULL)
    }
  })
}

###### input the df (e.g. merged_final_sorted) and column name (e.g. sample_1) #####
get_TPM<-function(df,sample){

  RPK<-df[[sample]]* 1000 / df$gene_length
  scalingFactor <- sum(RPK)
  TPM<-RPK/(scalingFactor/1e6)
  
  return(TPM)
}

